    private void cargarPaquetes(){
        cmbPaquete.removeAllItems();
        NodoPaquetes aux = lista.getInicio();
        while (aux != null){
            cmbPaquete.addItem(aux.getDato().getDescripcion());
            aux = aux.getSiguiente();
        }
    }

